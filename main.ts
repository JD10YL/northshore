function checkForCollision () {
    if (rock01) {
        if (rock01.overlapsWith(rider)) {
            if (Math.abs(rider.bottom - rock01.bottom) < 3) {
                scene.cameraShake(2, 300)
                decreaseHealth()
            }
        }
    }
    if (tree01) {
        if (tree01.overlapsWith(rider)) {
            rider.z = rider.bottom
            if (Math.abs(rider.bottom - tree01.bottom) < 3) {
                scene.cameraShake(3, 400)
                decreaseHealth()
            }
        }
    }
}
function keepGroodClose () {
    if (grood && duke && rider) {
        if (currentLevel == 4) {
            partWayThereX = 0.03 * (duke.x - 12 - grood.x)
            partWayThereY = 0.03 * (duke.y - 8 - grood.y)
            if (grood.x >= 90) {
                grood.x = 90
            }
            if (grood.y >= duke.y - 8) {
                grood.y = duke.y - 8
            }
            if (grood.x > rider.x - 40) {
                grood.x = rider.x - 40
            }
            grood.setPosition(partWayThereX + grood.x, partWayThereY + grood.y)
            grood.z = grood.bottom
        }
    }
}
function checkGameOver () {
    if (numHealth == 0 && game.runtime() - timeOfLastCrash >= 300) {
        game.showLongText("        OUCH!!              Better luck            next time!", DialogLayout.Top)
        effects.blizzard.startScreenEffect()
        game.reset()
    }
}
function keepDukeClose () {
    if (duke) {
        partWayThereX = 0.05 * (rider.x - 32 - duke.x)
        partWayThereY = 0.05 * (rider.y - -3 - duke.y)
        if (duke.x >= 70) {
            duke.x = 70
        }
        if (duke.y <= rider.y + 3) {
            duke.y = rider.y + 3
        }
        duke.setPosition(partWayThereX + duke.x, partWayThereY + duke.y)
        duke.z = duke.bottom
    }
}
